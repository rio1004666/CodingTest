# 최대 수익
# 주식을 사면 배당금을 받을 수 있다. 1주당 m의 배당금을 받을 수 있다. 나는 k의 자본금을 가지고 있을 때 배당금을 받았을 때 최대로 받을 수 있는 수익금을 출력하라.
#
# 입력은 n, k가 주어지고, n은 주식의 개수, k는 나의 자본금이다.
# 이후 n개의 줄에는 주식의 정보가 주어진다. 주식의 가격 p, 배당금 m, 주식의 개수 c 순으로 주어진다.
#
# 입력 예시 --
# 5 10
# 1 2 1
# 1 3 1
# 1 4 1
# 1 5 1
# 10 11 1
#
# 출력 예시 --
# 14
#
# 여기서 제한이
# N은 1000 M 10000
# K 물건갯수 최대 50